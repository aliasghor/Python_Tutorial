"""
    Cara membuat Variabel

    Variabel adalah tempat menyimpan data atau value
    nama_variabel assignment(=) value

"""

# Assignment nilai
x = 5
y = 10

print(f"Nilai x = {x}")
print(f"Nilai y = {y}")

# Penamaan variabel
menggunakanCamelCase = "menggunakanCamelCase"
print(menggunakanCamelCase)

menggunakan_under_score = "menggunakan_under_score"
print(menggunakan_under_score)


menggunakan_angka_dibelakangnya5000DanCamelCase = "menggunakan_angka_dibelakangnya5000DanCamelCase"
print(menggunakan_angka_dibelakangnya5000DanCamelCase)

pi = 3.141592653589
print(f"Nilai pi: {pi:.2f}")

nilai_jutaan = 10000000
print(f"Nilai jutaan = {nilai_jutaan:,}")
