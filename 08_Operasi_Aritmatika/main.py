# Pertambahan(+)
x = 10
y = -3
hasil = x + y
print(f"Hasil pertambahan = {hasil}")

# Pengurangan(-)
x = 10
y = -3
hasil = x - y
print(f"Hasil pengurangan = {hasil}")

# Pembagian(/)
x = 10
y = -3
hasil = x / y
print(f"Hasil pembagian = {hasil}")

# Perkalian(/)
x = 10
y = -3
hasil = x * y
print(f"Hasil perkalian = {hasil}")

# Exponent(/)
x = 5
y = 3
hasil = x ** y
print(f"Hasil exponent = {hasil}")

# Modulus(%)
x = 5
y = 3
hasil = x % y
print(f"Hasil modulus = {hasil}")

# Operator precedence

hasil = x * y ** x // x / y % x + y - x
print(f"Hasil Operator precedence = {hasil}")

hasil = (x + y) / y * x
print(f"Hasil Operator precedence = {hasil}")

